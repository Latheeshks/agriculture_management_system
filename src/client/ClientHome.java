    public ClientHome(String uname ) {
        itemshash = new HashMap<String, Item>();
        this.Username = uname;
        initComponents();
        userName.setText("Hi "+this.Username);
        ResultSet rs = new db.dbconnect().getData("select * from stock");
        try {
            while(rs.next()){
                Item it = new Item(rs.getString("s_id"),rs.getString("s_name"),rs.getString("s_amount"),rs.getString("s_count"));
                itemshash.put(rs.getString("s_id"), it);
                
                it.add_to_cart.addActionListener(new ActionListener() {

                    @Override
                    public void actionPerformed(ActionEvent e) {
                       if(Integer.parseInt(it.avail_stock.getText())!=0){
                        int flag =0;
                        DefaultTableModel dm = (DefaultTableModel)jTable1.getModel();
                        int rowcount = dm.getRowCount();
                        for(int i = 0;i<rowcount;i++){
                            if(it.id_stock.equals(jTable1.getValueAt(i, 0))){
                                flag = 1;
                                int a =  Integer.parseInt(String.valueOf(jTable1.getValueAt(i, 2))) ;
                                int b = a+1;
                                jTable1.setValueAt(""+b, i, 2);
                                int amount = Integer.parseInt(it.s_amount);
                                int Totamount = amount*b;
                                jTable1.setValueAt(""+Totamount, i, 3);
                            }
                        }
                        if(flag == 0){
                        dm.addRow(new Object[]{
                            it.id_stock,
                            it.s_name,
                            1,
                            it.s_amount
                        });
                        }
                        checkoutAmount = Integer.parseInt(it.s_amount);
                        checkoutAmount += Integer.parseInt(Total_amount.getText());
                        Total_amount.setText(String.valueOf(checkoutAmount));
                    }else{
                           System.out.println("out of stock");
                       }
                }
                });
                jPanel1.add(it);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ClientHome.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    }

