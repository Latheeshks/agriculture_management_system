    private void checkoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkoutActionPerformed

        
        ArrayList<HashMap<String, String>> mEntries = new ArrayList<HashMap<String, String>>();
        DefaultTableModel dm1 = (DefaultTableModel)jTable1.getModel(); 
        int cntrow = dm1.getRowCount();
        if(cntrow<=0){
            JOptionPane.showMessageDialog(rootPane, "No entries...");
        }else{
        for(int i = 0;i<cntrow ; i++){
            HashMap map = new HashMap();
            map.put("item_id", jTable1.getValueAt(i, 0));
            map.put("item_name", jTable1.getValueAt(i, 1));
            map.put("item_quantity", jTable1.getValueAt(i, 2));
            map.put("username", Username);
            mEntries.add(map);
        }
        new checkout(this,mEntries, Total_amount.getText(),Username).setVisible(true);
        }
    }//GEN-LAST:event_checkoutActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        try{
        DefaultTableModel dm2 = (DefaultTableModel)jTable1.getModel();
            int numRows = jTable1.getSelectedRows().length;
            Item its = itemshash.get(jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            its.avail_stock.setText(""+(Integer.parseInt(its.avail_stock.getText())+Integer.parseInt(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 2)))));
            
            int balAmnt = Integer.parseInt(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 3)));
            int finalbal = Integer.parseInt(Total_amount.getText())-balAmnt;
            Total_amount.setText(String.valueOf(finalbal));
            
            for(int i=0; i<numRows ; i++ ) {
                dm2.removeRow(jTable1.getSelectedRow());
            }
        }catch(Exception e){
             Logger.getLogger(ClientHome.class.getName()).log(Level.SEVERE, null, e);
//            JOptionPane.showMessageDialog(rootPane, "select row..","",ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteActionPerformed

